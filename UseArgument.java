public class UseArgument
{
	public static void main(String[] args)
	{
		System.out.print("Hi, ");
		System.out.print(args[0] );
		System.out.print(", ");
		System.out.print(args[1] );
		System.out.print(", ");
		System.out.print(args[2] );
		System.out.print(", ");
		System.out.println(". How are you?");
	} 
}